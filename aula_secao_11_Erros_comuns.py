"""
Erros mais comuns em Python

SyntaxErro - Ocorre quando o Python encontra um erro de sintaxe. Ou seja,
você escreveu algo que o Python não reconhece como parte da linguagem.

NameError - Ocorre quando uma variável ou função não foi definida.

TypeError - Ocorre quando uma função, operação, ação é aplicada em um tipo
errado.

IndexError - Ocorre quando tentamos acessar um elemento em uma lista ou
outro tipo de dado indexado utilizando um indice inválido.

ValueError - Ocorre quando uma função, operação, built-in (integrado) recebe
um argumento com tipo correto mas valor inapropriado.

KeyError - Ocorre quando tentamos acionar um dicionário com uma chave que não
existe.

AttributeError - Ocorre quando uma variável não te um atributo/função.

IndentationError - Ocorre quando não respeitamos a indentação do Python
(4 espaços).
"""